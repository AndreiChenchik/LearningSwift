class Phone: SolutionProtocol {
    var diagonal: Double
    var os: OS
    var name: String
    
    init(diagonal: Double, os: OS, name: String) {
        self.diagonal = diagonal
        self.os = os
        self.name = name
    }
    
    func call() -> String {
        "\(name) \(os) \(diagonal)"
    }
    
    func rename(newName: String) {
        name = newName
    }
    
    func update(newOS: OS) {
        os = newOS
    }
}

